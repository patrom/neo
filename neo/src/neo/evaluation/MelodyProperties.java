package neo.evaluation;

public class MelodyProperties {

	private int position;
	private int length;
	
	public MelodyProperties(int position, int length) {
		super();
		this.position = position;
		this.length = length;
	}
	
	public int getPosition() {
		return position;
	}
	public int getLength() {
		return length;
	}
	
}
